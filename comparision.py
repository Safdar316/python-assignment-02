# COMPARISION OR RELATIONAL OPERATOR

#  1.. equal (==)
numeric_val_1 = 9
numeric_val_2 = 19
print(numeric_val_1 == numeric_val_2)  # False

# 2.. not equal (!=)
numeric_val_3 = 56
numeric_val_4 = 6
print(numeric_val_3 != numeric_val_4)  # True

# 3.. greater than (>)
numeric_val_5 = 98
numeric_val_6 = 78
print(numeric_val_5 > numeric_val_6)  # True

# 4.. less than (<)
# 5.. greater than (>)
numeric_val_7 = 45
numeric_val_8 = 65
print(numeric_val_7 < numeric_val_8)  # True
print(numeric_val_7 > numeric_val_8)  # False

# 6.. greater than or equal to (>=)
# 6.. less than or equal to (<=)
num_int_1 = 56
num_int_2 = 65
print(num_int_1 >= num_int_2) # Fale 
print(num_int_1 <= num_int_2) # True 