#  LOGICAL OPERATORS

# 1.. (and)
valu_1 = 23
valu_2 = 32
print( valu_1 > valu_2 and valu_1 < valu_2 ) # False 

# 2.. (or)
valu_3 = 53
valu_4 = 92
print( valu_3 > valu_4 or valu_3 < valu_4 ) # True 

# 3.. (not)
valu_5 = 53
valu_6 = 92
print( not(valu_5 > valu_6) ) # True 

print( 5 + 8.9)